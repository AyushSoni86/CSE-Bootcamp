class CharacterDemo {
    public static void main(String[] args){
        char ch1 = 'a';
        char ch2 = '1';
        char ch3 = '@';
        char ch4 = '%';
        char ch5 = '.';

        System.out.println(ch1 + ch2);
        System.out.println(ch2);
        System.out.println(ch3);
        System.out.println(ch4);
        System.out.println(ch5);
    }
}
