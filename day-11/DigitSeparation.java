class DigitSeparation {
    public static void main(String[] args) {
        int n = 123456;
        // 123456/10
        // quotient - 12345
        // remainder - 6

        System.out.println(123456 / 10);
        System.out.println(123456 % 10);

        System.out.println(1 / 10); // --> 0
        System.out.println(1 % 10); // -> 0

        System.out.println(10 / 1); // 10
        System.out.println(10 % 1); // 0
    }
}
